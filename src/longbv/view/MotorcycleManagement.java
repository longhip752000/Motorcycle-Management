    private void btnSaveBrandActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveBrandActionPerformed
        String brandID = this.txtBrandID.getText();
        String brandName = this.txtBrandName.getText();
        String country = this.txtCountry.getText();
        String description = this.txtDescription.getText();
        BrandRequest request = new BrandRequest(brandID, brandName, country, description);
        Notification noti = BrandController.saveBrand(request, mode);
        if (noti.isResult()) {
            if (mode == ADD_MODE) {
                boolean result = bDao.addBrand((BrandDTO) noti.getData());
                if (result) {
                    JOptionPane.showMessageDialog(this, "Add successfully!");
                    bModel.getData().add((BrandDTO) noti.getData());
                } else {
                    JOptionPane.showMessageDialog(this, "Add failed!");
                }
            } else {
                boolean result = bDao.updateBrand((BrandDTO) noti.getData());
                if (result) {
                    JOptionPane.showMessageDialog(this, "Update successfully!");
                    bModel.getData().add((BrandDTO) noti.getData());
                } else {
                    JOptionPane.showMessageDialog(this, "Update failed!");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, noti.getMsg());
        }
        bModel.setData(brands = bDao.getAll());
        mModel.setData(motors = mDao.getAll());
        loadComboBox(brands);
        tblBrand.updateUI();
    }//GEN-LAST:event_btnSaveBrandActionPerformed

    private void btnSaveMotorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveMotorActionPerformed
       String motorID = this.txtMotorID.getText();
        String model = this.txtModel.getText();
        String year = this.txtYear.getText();
        String condition = this.txtCondition.getText();
        String priceStr = this.txtPrice.getText();
        String quantityStr = this.txtQuantity.getText();
        String warranty = this.txtWarranty.getText();
        String brandID = ((String) this.cbBrandName.getModel().getSelectedItem()).split("-")[0];
        MotorcycleRequest request = new MotorcycleRequest(motorID, model, year, condition, priceStr, quantityStr, warranty, brandID);
        Notification noti = MotorcycleController.saveMotor(request, mode);
        if (noti.isResult()) {
            if (mode == ADD_MODE) {
                boolean result = mDao.addMotor((MotorcycleDTO) noti.getData());
                if (result) {
                    JOptionPane.showMessageDialog(this, "Add successfully!");
                    mModel.getData().add((MotorcycleDTO) noti.getData());
                } else {
                    JOptionPane.showMessageDialog(this, "Add failed!");
                }
            } else {
                boolean result = mDao.updateMotor((MotorcycleDTO) noti.getData());
                if (result) {
                    JOptionPane.showMessageDialog(this, "Update successfully!");
                    mModel.getData().add((MotorcycleDTO) noti.getData());
                    mode = ADD_MODE;
                } else {
                    JOptionPane.showMessageDialog(this, "Update failed!");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, noti.getMsg());
        }
        mModel.setData(motors = mDao.getAll());
        bModel.setData(brands = bDao.getAll());
        tblMotor.updateUI();
    }//GEN-LAST:event_btnSaveMotorActionPerformed

    private void btnDeleteBrandActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteBrandActionPerformed
        boolean check;
        if (JOptionPane.showConfirmDialog(this, "Are you sure to delete this brand?") == JOptionPane.OK_OPTION) {
            String brandID = this.txtBrandID.getText();
            check = mDao.checkMotorExistInBrand(brandID);

            if (check) {
                JOptionPane.showMessageDialog(null, "You must delete motorcycle has this brand ID first");
            } else {
                boolean result = bDao.deleteBrand(brandID);
                if (result) {
                    JOptionPane.showMessageDialog(null, "Delete Successfully!");
                    this.btnDeleteBrand.setEnabled(OFF);
                    mode = ADD_MODE;
                } else {
                    JOptionPane.showMessageDialog(null, "Delete Failed!");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Do not delete!");
        }

        bModel.setData(brands = bDao.getAll());
        mModel.setData(motors = mDao.getAll());
        loadComboBox(brands);
        tblBrand.updateUI();
    }//GEN-LAST:event_btnDeleteBrandActionPerformed

