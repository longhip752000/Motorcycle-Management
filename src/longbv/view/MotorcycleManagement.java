    public void loadComboBox(ArrayList<BrandDTO> brands) {
        DefaultComboBoxModel cbbName = new DefaultComboBoxModel();
        for (BrandDTO brand : brands) {
            cbbName.addElement(brand.getBrandID() + "-" + brand.getBrandName());
        }
        cbBrandName.setModel(cbbName);
    }

    public void showDataWhenMouseClickOnTableBrand() {
        int pos = tblBrand.getSelectedRow();
        BrandDTO curBrand = ((ListBrands<BrandDTO>) tblBrand.getModel()).getData().get(pos);
        this.txtBrandID.setText(curBrand.getBrandID());
        this.txtBrandName.setText(curBrand.getBrandName());
        this.txtCountry.setText(curBrand.getCountry());
        this.txtDescription.setText(curBrand.getDescription());
        this.txtBrandID.setEditable(false);
    }

    public void showDataWhenMouseClickOnTableMotor() {
        int pos = tblMotor.getSelectedRow();
        MotorcycleDTO curMotor = ((ListMotors<MotorcycleDTO>) tblMotor.getModel()).getData().get(pos);
        this.txtMotorID.setText(curMotor.getMotorID());
        this.txtModel.setText(curMotor.getModel());
        this.txtYear.setText(new SimpleDateFormat("MM-dd-yyyy").format(curMotor.getYear()));
        this.txtCondition.setText(curMotor.getCondition());
        this.txtPrice.setText(curMotor.getPrice() + "");
        this.txtQuantity.setText(curMotor.getQuantity() + "");
        this.txtWarranty.setText(curMotor.getWarranty());
        this.cbBrandName.getModel().setSelectedItem(curMotor.getBrandID() + "-" + bDao.getBrandNameByID(curMotor.getBrandID()));
        this.txtMotorID.setEditable(false);
    }
